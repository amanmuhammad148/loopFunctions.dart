// Implement a code that checks whether a given number is prime or not.
// Example:
// Input: 17
// Output: 17 is a prime number.

void main(){
  int primeNum = 17;
  if (primeNum <= 17) {
    print("17 is not prime number");
  }else{
    for (var i = 2; i <= 17; ++i) {
      print(primeNum);
    }
  }
  
} 